<?php

namespace Jet\ShopBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Jet\ShopBundle\Entity\User
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class User
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string $nick
     *
     * @ORM\Column(name="nick", type="string", length=20)
     */
    private $nick;

    /**
     * @var string $password
     *
     * @ORM\Column(name="password", type="string", length=20)
     */
    private $password;

    /**
     * @var string $name
     *
     * @ORM\Column(name="name", type="string", length=20)
     */
    private $name;

    /**
     * @var string $surname
     *
     * @ORM\Column(name="surname", type="string", length=100)
     */
    private $surname;

    /**
     * @var string $adress
     *
     * @ORM\Column(name="adress", type="string", length=100)
     */
    private $adress;

    /**
     * @var integer $numero
     *
     * @ORM\Column(name="numero", type="integer")
     */
    private $numero;

    /**
     * @var integer $piso
     *
     * @ORM\Column(name="piso", type="integer")
     */
    private $piso;

    /**
     * @var string $puerta
     *
     * @ORM\Column(name="puerta", type="string", length=5)
     */
    private $puerta;

    /**
     * @var string $poblacion
     *
     * @ORM\Column(name="poblacion", type="string", length=50)
     */
    private $poblacion;

    /**
     * @var string $provincia
     *
     * @ORM\Column(name="provincia", type="string", length=50)
     */
    private $provincia;

    /**
     * @var integer $codigo_postal
     *
     * @ORM\Column(name="codigo_postal", type="integer")
     */
    private $codigo_postal;

    /**
     * @var string $email
     *
     * @ORM\Column(name="email", type="string", length=120)
     */
    private $email;

    /**
     * @var date $date-registro
     *
     * @ORM\Column(name="date_registro", type="date")
     */
    private $date_registro;

    /**
     * @var boolean $publicidad
     *
     * @ORM\Column(name="publicidad", type="boolean")
     */
    private $publicidad;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nick
     *
     * @param string $nick
     */
    public function setNick($nick)
    {
        $this->nick = $nick;
    }

    /**
     * Get nick
     *
     * @return string 
     */
    public function getNick()
    {
        return $this->nick;
    }

    /**
     * Set password
     *
     * @param string $password
     */
    public function setPassword($password)
    {
        $this->password = $password;
    }

    /**
     * Get password
     *
     * @return string 
     */
    public function getPassword()
    {
        return $this->password;
    }

    /**
     * Set name
     *
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set surname
     *
     * @param string $surname
     */
    public function setSurname($surname)
    {
        $this->surname = $surname;
    }

    /**
     * Get surname
     *
     * @return string 
     */
    public function getSurname()
    {
        return $this->surname;
    }

    /**
     * Set adress
     *
     * @param string $adress
     */
    public function setAdress($adress)
    {
        $this->adress = $adress;
    }

    /**
     * Get adress
     *
     * @return string 
     */
    public function getAdress()
    {
        return $this->adress;
    }

    /**
     * Set numero
     *
     * @param integer $numero
     */
    public function setNumero($numero)
    {
        $this->numero = $numero;
    }

    /**
     * Get numero
     *
     * @return integer 
     */
    public function getNumero()
    {
        return $this->numero;
    }

    /**
     * Set piso
     *
     * @param integer $piso
     */
    public function setPiso($piso)
    {
        $this->piso = $piso;
    }

    /**
     * Get piso
     *
     * @return integer 
     */
    public function getPiso()
    {
        return $this->piso;
    }

    /**
     * Set puerta
     *
     * @param string $puerta
     */
    public function setPuerta($puerta)
    {
        $this->puerta = $puerta;
    }

    /**
     * Get puerta
     *
     * @return string 
     */
    public function getPuerta()
    {
        return $this->puerta;
    }

    /**
     * Set poblacion
     *
     * @param string $poblacion
     */
    public function setPoblacion($poblacion)
    {
        $this->poblacion = $poblacion;
    }

    /**
     * Get poblacion
     *
     * @return string 
     */
    public function getPoblacion()
    {
        return $this->poblacion;
    }

    /**
     * Set provincia
     *
     * @param string $provincia
     */
    public function setProvincia($provincia)
    {
        $this->provincia = $provincia;
    }

    /**
     * Get provincia
     *
     * @return string 
     */
    public function getProvincia()
    {
        return $this->provincia;
    }

    /**
     * Set codigo_postal
     *
     * @param integer $codigoPostal
     */
    public function setCodigoPostal($codigoPostal)
    {
        $this->codigo_postal = $codigoPostal;
    }

    /**
     * Get codigo_postal
     *
     * @return integer 
     */
    public function getCodigoPostal()
    {
        return $this->codigo_postal;
    }

    /**
     * Set email
     *
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * Get email
     *
     * @return string 
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * Set date_registro
     *
     * @param date $dateRegistro
     */
    public function setDateRegistro($dateRegistro)
    {
        $this->date_registro = $dateRegistro;
    }

    /**
     * Get date-registro
     *
     * @return date 
     */
    public function getDateRegistro()
    {
        return $this->date_registro;
    }

    /**
     * Set publicidad
     *
     * @param boolean $publicidad
     */
    public function setPublicidad($publicidad)
    {
        $this->publicidad = $publicidad;
    }

    /**
     * Get publicidad
     *
     * @return boolean 
     */
    public function getPublicidad()
    {
        return $this->publicidad;
    }
}